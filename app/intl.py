def get_lang(lang):
    return LANGUAGES.get(lang, LANGUAGES[DEFAULT_LANG])

def supported_lang(lang):
    return lang in LANGUAGES

def lang_codes():
    return list(LANGUAGES.keys())

def localized_tool(tool, lang):
    title, desc = TOOL_TRANSLATIONS.get(tool["slug"], {}).get(lang, (tool["name"], tool["desc"]))
    item = dict(tool)
    item["name_local"] = title
    item["desc_local"] = desc
    return item

def global_hreflang(domain, current_path_builder):
    links = []
    for code in LANGUAGES:
        links.append({"lang": code, "url": domain + current_path_builder(code)})
    links.append({"lang": "x-default", "url": domain + current_path_builder(DEFAULT_LANG)})
    return links
